// Emojis de bandeira para Inglaterra e Escócia (subdivisões do Reino Unido).
// Usam tag sequences de Unicode, não os indicadores regionais de país.
const SUBDIVISION_FLAGS: Record<string, string> = {
  'GB-ENG': '\u{1F3F4}\u{E0067}\u{E0062}\u{E0065}\u{E006E}\u{E0067}\u{E007F}',
  'GB-SCT': '\u{1F3F4}\u{E0067}\u{E0062}\u{E0073}\u{E0063}\u{E0074}\u{E007F}',
};

/**
 * Converte um código de país em emoji de bandeira.
 * Para códigos ISO alpha-2, soma o offset dos indicadores regionais
 * (A = U+1F1E6). Para subdivisões (Inglaterra/Escócia), usa a tabela acima.
 */
export function flagEmoji(code: string): string {
  const upper = code.trim().toUpperCase();
  if (SUBDIVISION_FLAGS[upper]) return SUBDIVISION_FLAGS[upper];
  if (!/^[A-Z]{2}$/.test(upper)) return '';
  const base = 0x1f1e6 - 'A'.charCodeAt(0);
  return String.fromCodePoint(base + upper.charCodeAt(0), base + upper.charCodeAt(1));
}

